#include<unistd.h>

void	ft_print_comb2(void)
{
	int		i;
	char	a[4];

	i = 0;
	while (i < 10000)
	{
		if (i == 9899)
			write(1, "98 99", 5);
		else if (i / 100 < i % 100)
		{
			a[0] = '0' + i / 1000;
			a[1] = '0' + i % 1000 / 100;
			a[2] = '0' + i % 100 / 10;
			a[3] = '0' + i % 10;
			write(1, &a[0], 1);
			write(1, &a[1], 1);
			write(1, " ", 1);
			write(1, &a[2], 1);
			write(1, &a[3], 1);
			write(1, ", ", 2);
		}
		i++;
	}
}
